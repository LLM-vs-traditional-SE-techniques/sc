    BitArray bits = new BitArray(new int[] {1, 0, 1, 0}, 4);
    bits.writeBytesTo(dest, 0, 4);
    assertEquals(new byte[] {0x0a, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00}, dest);
    bits.writeBytesTo(dest, 1, 3);
    assertEquals(new byte[] {0x0a, 0x02, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00}, dest);
